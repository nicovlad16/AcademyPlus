#include "libft.h"

int		ft_atoi(const char *str)
{
	int i;
	int sign;
	int nr;

	i = 0;
	nr = 0;
	while (str[i] == ' ' || (str[i] >= 9 && str[i] <= 13))
		i++;
	if (str[i] == '+')
		i++;
	else if (str[i] == '-')
	{
		sign = -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		nr = nr * 10 + (str[i] - '0');
		i++;
	}
	if (sign == -1)
		nr = nr * -1;
	return (nr);
}
