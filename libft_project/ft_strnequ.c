#include "libft.h"

int		ft_strnequ(char const *s1, char const *s2, size_t n)
{
	size_t	i;

	i = 0;
	if (s1 != NULL && s2 != NULL)
	{
		while (i < n)
		{
			if (s1[i] != s2[i] && s1[i] != '\0')
				return (0);
			i++;
		}
	}
	return (1);
}
