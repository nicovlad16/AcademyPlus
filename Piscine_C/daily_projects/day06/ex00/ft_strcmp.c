int		ft_strcmp(char *s1, char *s2)
{
	int				i;
	unsigned char	*s3;
	unsigned char	*s4;

	i = 0;
	s3 = (unsigned char *)s1;
	s4 = (unsigned char *)s2;
	while (s3[i] == s4[i] && s3[i] != '\0' && s4[i] != '\0')
		i++;
	return (s3[i] - s4[i]);
}
